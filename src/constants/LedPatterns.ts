export const LED_CIRCLE_PATTERN: number[][] = LED_CIRCLE_PATTERN_BASE.slice(0, 12).map(row => row.slice(0, 12));

// Daire içinde rakam pattern'i oluştur
export const getCircleWithDigitPattern = (digit: number): number[][] => {
  const digitPattern = digitPatterns[digit] || digitPatterns[1]; // default to 1 if not found
  return LED_CIRCLE_PATTERN_BASE.map((row, rowIndex) =>
    row.map((circlePixel, colIndex) => {
      // Check if rowIndex and colIndex are within bounds for digitPattern
      const digitPixel = (digitPattern[rowIndex] && digitPattern[rowIndex][colIndex] !== undefined)
        ? digitPattern[rowIndex][colIndex]
        : 0; // Default to 0 if out of bounds
      // Eğer digit pixel 1 ise, digit'i göster, yoksa circle'ı göster
      return digitPixel === 1 ? 2 : circlePixel; // 2 for digit, 1 for circle
    })
  );
};
